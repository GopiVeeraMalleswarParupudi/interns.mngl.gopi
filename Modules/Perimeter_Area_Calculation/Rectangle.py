def area(length,width):
    print('*'*80)
    print('The area of rectangle having length{} and width{} : {}'.format(length,width,length*width))
    print('*'*80)

def perimeter(length,width):
    print('*'*80)
    print('The area of square having length{} and width{} : {}'.format(length,width,2*(length+width)))
    print('*'*80)