def area(base,height):
    print('*'*80)
    print('The area of triangle having base{} and height{} : {}'.format(base,height,(1/2)*(base*height)))
    print('*'*80)

def perimeter(side1,side2,side3):
    print('*'*80)
    print('The area of triangle having lengths of three sides are ({},{},{}) : {}'.format(side1,side2,side3,(side1+side2+side3)))
    print('*'*80)